function convertTcvn3ToUnicode(input) {
    const tcvn3 = [
      '¸','µ','¶','·','¹','¨','»','¾','¼','½','Æ',
      '©','Ç','Ê','È','É','Ë','®','Ì','Ð','Î','Ï','Ñ',
      'ª','Ò','Õ','Ó','Ô','Ö','×','Ý','Ø','Ü','Þ','ß',
      'ã','á','â','ä','«','å','è','æ','ç','é','¬','ê','í','ë','ì','î','ï',
      'ó','ñ','ò','ô','­','õ','ø','ö','÷','ù','ú','ý','û','ü','þ','¡',
      '§','£','¢','¤','¥'
    ];
  
    const unicode = [
      'à','á','ả','ã','ạ','ă','ắ','ằ','ẳ','ẵ','ặ',
      'â','ấ','ầ','ẩ','ẫ','ậ','đ','è','é','ẻ','ẽ','ẹ',
      'ê','ế','ề','ể','ễ','ệ','ì','í','ỉ','ĩ','ị','ò',
      'ó','ỏ','õ','ọ','ô','ố','ồ','ổ','ỗ','ộ','ơ','ớ','ờ','ở','ỡ','ợ',
      'ù','ú','ủ','ũ','ụ','ư','ứ','ừ','ử','ữ','ự','ý','ỳ','ỷ','ỹ','ỵ',
      'Ă','Â','Đ','Ê','Ô','Ơ','Ư','À','Á','Ả','Ã','Ạ','Ấ','Ầ','Ẩ','Ẫ','Ậ',
      'É','È','Ẻ','Ẽ','Ẹ','Ế','Ề','Ể','Ễ','Ệ','Í','Ì','Ỉ','Ĩ','Ị',
      'Ó','Ò','Ỏ','Õ','Ọ','Ố','Ồ','Ổ','Ỗ','Ộ','Ơ','Ớ','Ờ','Ở','Ỡ','Ợ',
      'Ú','Ù','Ủ','Ũ','Ụ','Ứ','Ừ','Ử','Ữ','Ự','Ý','Ỳ','Ỷ','Ỹ','Ỵ'
    ];
  
    return input.split('').map(char => {
      const index = tcvn3.indexOf(char);
      return index !== -1 ? unicode[index] : char;
    }).join('');
  }
  
  module.exports = convertTcvn3ToUnicode;
  